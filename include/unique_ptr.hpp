///////////Get////////////////////////////////////////////////////////////////////////
template<typename T>
T* UniquePtr<T>::get()
{
    return _p;
}
///////////reset1/////////////////////////////////////////////////////////////////////
template<typename T>
void UniquePtr<T>::reset()
{
    delete _p;
    _p = nullptr;
}
///////////reset2/////////////////////////////////////////////////////////////////////
template<typename T>
void UniquePtr<T>::reset(T* p)
{
    delete _p;
    _p = p;
}
///////////Operator Bool//////////////////////////////////////////////////////////////
template<typename T>
UniquePtr<T>::operator bool()
{
    if(_p)
        return true;
    return false;
}
///////////Release//////////////////////////////////////////////////////////////
template<typename T>
T* UniquePtr<T>::release()
{
    T* outpt = _p;
    _p = nullptr;
    return outpt;
}