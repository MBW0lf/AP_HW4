///////////Deconstructor//////////////////////////////////////////////////////////////
template<typename T>
UniquePtr<T> :: ~UniquePtr() 
{
    delete _p;
    _p = nullptr;
}
///////////Operator*//////////////////////////////////////////////////////////////////
template<typename T>
T& UniquePtr<T>::operator*()
{
    return (*_p);
}
///////////Operator->/////////////////////////////////////////////////////////////////
template<typename T>
T* UniquePtr<T>::operator->()
{
    return _p;
}
