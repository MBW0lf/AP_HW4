///////////Constructor////////////////////////////////////////////////////////////////
template<typename T>
UniquePtr<T> :: UniquePtr() : _p{nullptr}{};
///////////Constructor////////////////////////////////////////////////////////////////
template<typename T>
UniquePtr<T> :: UniquePtr(T* p) : _p{p}{};
